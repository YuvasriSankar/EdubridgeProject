
public class Calculator {
	public int add(int i,int j,int k) {
		int add=i+j+k;
		return add;
	}
	public int sub(int i,int j,int k) {
		int sub=i-j-k;
		return sub;
	}
	public int mul(int i,int j,int k) {
		int mul=i*j*k;
		return mul;
	}

	public int div(int i,int j,int k) {
		int div=i/j/k;
		return div;
	}
	public int square(int i,int j) {
		int square=(i)^2;
		return square;
	}
	public int remindor(int i,int j) {
		int remindor=i/j;
		return remindor;
	}
	public int 	quotient(int i,int j) {
		int quotient=i/j;
		return quotient;
	}
}
		